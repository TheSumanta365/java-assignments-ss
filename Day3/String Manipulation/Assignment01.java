public class Assignment01{
    public static void main(String[] args) {
       String s="Umbrella";
for(int i=0;i<s.length();i++)
{
if(s.charAt[i].contains("e"))
{
System.out.print("present");
            }
else
System.out.print("not present");

        }
    }
}